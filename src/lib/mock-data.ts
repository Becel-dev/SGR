const getStatusCounts = (risks: Risk[]) => {
  const counts: { [key: string]: number } = {
    'Aberto': 0,
    'Em Tratamento': 0,
    'Fechado': 0,
    'Mitigado': 0,
  };
  risks.forEach(risk => {
    const status = risk.statusDoRisco;
    if (counts.hasOwnProperty(status)) {
      counts[status]++;
    }
  });
  return counts;
};

